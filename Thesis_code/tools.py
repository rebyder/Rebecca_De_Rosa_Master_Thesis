class WebSearchTool(BaseModel):
    """
    Tool to perform web searches and return top 5 results.
    
    Args:
        query (str): search query string
        cwe (str): optional target CWE for routing search memory (e.g. "CWE-89")
    """

    query: str = Field(description="The search query (e.g., 'CodeQL python CWE-89 sink source names')")
    cwe: str = Field(default="", description="Optional CWE id associated with this search (e.g., 'CWE-89').")

    def run(self) -> str:
        """
        Performs the search and returns results as JSON with retry logic.

        Returns:
            str: JSON string of search results or error message
        """
        max_retries = 3
        base_delay = 2  # seconds

        for attempt in range(max_retries):
            try:
                results = DDGS().text(f"site:codeql.github.com OR site:github.com {self.query}", max_results=3)
                h = html2text.HTML2Text()  # in modo da preservare la sintassi di codeql
                h.ignore_links = True
                h.ignore_images = True

                final_context = []

                for res in results:
                    url = res['href']
                    try:
                        response = requests.get(url, timeout=10)
                        response.raise_for_status()  # Raise exception for bad status codes
                        soup = BeautifulSoup(response.text, 'html.parser')

                        for junk in soup(['nav', 'footer', 'header', 'script', 'style']):
                            junk.decompose()

                        texy_md = h.handle(str(soup))

                        final_context.append(f"SOURCE: {url}\nCONTENT:\n{texy_md[:3000]}")
                    except requests.exceptions.RequestException as e:
                        print(f"Warning: Failed to fetch {url}: {str(e)}")
                        continue
                    except Exception as e:
                        print(f"Warning: Error processing {url}: {str(e)}")
                        continue

                if final_context:
                    return "\n---\n".join(final_context)
                else:
                    return "No results could be fetched from the search."

            except Exception as e:
                if attempt < max_retries - 1:
                    delay = base_delay * (2 ** attempt)  # Exponential backoff
                    print(f"Search attempt {attempt + 1} failed: {str(e)}. Retrying in {delay}s...")
                    time.sleep(delay)
                else:
                    return f"Error: All {max_retries} search attempts failed. Last error: {str(e)}"

        return "Error: Maximum retries exceeded"

