"""
Module that implements all the tools used by the Agents.

Tools:
    - CodeQLSastTool: created a CodeQL database and run SAST analysis 
    - ParseSarifTool: parses the SARIF reports into structured JSON fro LLM
    - WebSearchTool: performs web searches and returns the top 5 results
    - WriteQuerySubAgent: generates CodeQL queries for a given CWE
    - FinishTool: marks the completion of the CreatorAgent
    - FinishToolSuggestor: marks the completion of the SuggestorAgent

Workflow:
    1. Source code is analyzed using CodeQLSastTool
    2. SARIF results are parsed by ParseSarifTool
    3. WebSearchTool can be invoked to gather context or insights
    4. CreatorAgent uses WriteQuerySubAgent and FinishTool to generate queries iteratively.

Args:
    Each tool class takes specific arguments as defined in their respective docstrings.
"""

import os
import json
import requests
from bs4 import BeautifulSoup
import html2text
import re
import time

from typing import List, Dict, Any
from pydantic import BaseModel, Field


class ParseSarifTool(BaseModel):  
    """
    Tool to parse SARIF report and produced JSON results for LLM processing.

    Args:
        sarif_filepath (str): path to SARIF JSON file
    """

    sarif_filepath: str = Field(description="Path to the SARIF JSON file generated by CodeQL.")
    
    def extract_cwes_from_rule(self, rule: Dict[str, Any]) -> List[str]:
        """Extract CWE identifiers from a SARIF rule definition."""
        cwes: List[str] = []

        tags = rule.get("properties", {}).get("tags", []) or []
        for tag in tags:
            if isinstance(tag, str) and tag.lower().startswith("external/cwe/cwe-"):
                num = tag.split("external/cwe/cwe-")[-1]
                num = re.sub(r"\D", "", num)
                if num:
                    cwes.append(f"CWE-{num}")

        for rel in rule.get("relationships", []) or []:
            target_id = (rel.get("target") or {}).get("id")
            if isinstance(target_id, str) and target_id.upper().startswith("CWE-"):
                num = re.sub(r"\D", "", target_id)
                if num:
                    cwes.append(f"CWE-{num}")

        help_md = (rule.get("help") or {}).get("markdown") or ""
        help_txt = (rule.get("help") or {}).get("text") or ""
        blob = f"{help_md}\n{help_txt}"
        for m in re.findall(r"\bCWE-(\d{1,5})\b", blob, flags=re.IGNORECASE):
            cwes.append(f"CWE-{m}")

        seen = set()
        out: List[str] = []
        for c in cwes:
            if c not in seen:
                seen.add(c)
                out.append(c)
        return out


    def run(self) -> str:
        """
        Reads the SARIF file and returns structured JSON.
        
        Returns:
            str: structured JSON string of results.
        """
        if not os.path.exists(self.sarif_filepath):
            raise Exception(f"ERROR: File '{self.sarif_filepath}' does not exist.")
        
        with open(self.sarif_filepath, 'r', encoding='utf-8') as f:
            sarif_data = json.load(f)
        
        structured_res: List[Dict[str, Any]] = []
        
        for runn in sarif_data.get('runs', []):
            tool = runn.get('tool', {})
            tool_name = tool.get('driver', {}).get('name', 'CodeQL')

            
            rules_id: Dict[str, Dict[str, Any]] = {}
            components = []
            driver = tool.get("driver")
            if isinstance(driver, dict):
                components.append(driver)
            
            exts = tool.get("extensions", []) or []
            for ext in exts:
                if isinstance(ext, dict):
                    components.append(ext)

            for component in components:
                for rule in component.get('rules', []):
                    rid = rule.get('id')
                    if isinstance(rid, str) and rid:
                        rules_id[rid] = rule

            for result in runn.get('results', []):
                rule_id = result.get('ruleId') or 'unknown_rule'
                rule_def = rules_id.get(rule_id, {}) or {}
                tags = rule_def.get('properties', {}).get('tags', []) or []
              
                if tags and 'security' not in tags:
                    continue


                cwes = self.extract_cwes_from_rule(rule_def)
                if not cwes:
                    cwes = ["CWE-Unknown"]
               
                message = (result.get('message') or {}).get('text') or 'No description'
                
                locations = []

                for loc in result.get("locations", []) or []:
                    phys = loc.get('physicalLocation') or {}
                    art = phys.get('artifactLocation') or {}
                    reg = phys.get('region') or {}
                    locations.append({
                        "uri": art.get("uri", "unknown_file"),
                        "line": reg.get("startLine")
                    })
                    

                structured_res.append({
                    'tool': tool_name,
                    'rule_id': rule_id,
                    'cwe': cwes,
                    'message': message,
                    'locations': locations
                })
        
        if not structured_res:
            print("\n\nWARNING: no security findings extracted from SARIF.")

        return json.dumps(structured_res, indent=2)


